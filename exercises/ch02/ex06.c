#include <stdio.h>
int main(void){
    // 定义整型变量toes
    int toes;
    // 将toes设置为10
    toes = 10;
    printf("toes = %d\n", toes);
    // 计算toes的两倍
    printf("Twice toes = %d\n", 2 * toes);
    // 计算toes的平方
    printf("toes squared = %d\n", toes * toes);
    return 0;
}
